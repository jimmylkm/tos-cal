package element;

public class Gem {
	private String color;
	private boolean isAmp;
	
	public String getColor() {
		return color;
	}
	public void setColor(String color) {
		this.color = color;
	}
	public boolean isAmp() {
		return isAmp;
	}
	public void setAmp(boolean isAmp) {
		this.isAmp = isAmp;
	}

}
